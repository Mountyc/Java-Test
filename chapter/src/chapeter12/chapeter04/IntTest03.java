package chapeter12.chapeter04;
/*
    java语法规则
    当这个整数型字面量没有超出byte取值范围，那么这个
    整数型字面量可以直接赋值给byte类型的变量。

 */

public class IntTest03 {
    public static void main(String[] args){
        byte b = (byte)300;
        System.out.println(b);//44

        byte x = 1;
        System.out.println(x);

        short s = 1;
        System.out.println(s);
    }
}
